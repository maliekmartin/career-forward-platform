// Job Board Data
// Structure ready for API/scraping integration
// Currently using mock data for demo

export interface Job {
  id: string;
  title: string;
  company: string;
  location: string;
  description: string;
  requirements: string[];
  payMin: number;
  payMax: number;
  payType: "hourly" | "salary";
  hoursType: "full-time" | "part-time" | "contract" | "temporary";
  hoursPerWeek?: number;
  datePosted: string;
  source: "worksourcewa" | "indeed" | "linkedin" | "governmentjobs";
  sourceUrl: string;
  category: "healthcare" | "manufacturing" | "admin" | "retail" | "tech" | "other";
  remote: boolean;
  benefits?: string[];
  companyLogo?: string;
}

export interface JobApplication {
  id: string;
  jobId: string;
  job: Job;
  status: "applied" | "phone-screen" | "first-interview" | "final-interview" | "offered" | "hired" | "rejected";
  appliedDate: string;
  lastUpdated: string;
  notes?: string;
  // Hired outcome data
  hiredData?: {
    startDate: string;
    jobTitle: string;
    hourlyWage?: number;
    salary?: number;
    hoursPerWeek: number;
    successStory?: string;
  };
}

