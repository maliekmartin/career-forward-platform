// Helper function to format pay
export function formatPay(job: Job): string {
  if (job.payType === "hourly") {
    return `$${job.payMin} - $${job.payMax}/hr`;
  }
  return `$${(job.payMin / 1000).toFixed(0)}K - $${(job.payMax / 1000).toFixed(0)}K/yr`;
}

// Helper function to format date
export function formatJobDate(dateString: string): string {
  const date = new Date(dateString);
  const now = new Date();
  const diffTime = Math.abs(now.getTime() - date.getTime());
  const diffDays = Math.floor(diffTime / (1000 * 60 * 60 * 24));

  if (diffDays === 0) return "Today";
  if (diffDays === 1) return "Yesterday";
  if (diffDays < 7) return `${diffDays} days ago`;
  return date.toLocaleDateString();
}

